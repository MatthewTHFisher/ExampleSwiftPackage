import Foundation

/// Function to add two values together
/// - Parameter a: Integer
/// - Parameter b: Integer to add to a
func addition(a: Int, b: Int) -> Int {
    print(a+b)
    return a + b
}

/// Function to add two values together
/// - Parameter a: Integer
/// - Parameter b: Integer to subtract from a
func subtraction(a: Int, b: Int) -> Int {
    print(a-b)
    return a - b
}
